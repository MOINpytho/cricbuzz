            mailSender.setPort(587);
            mailSender.setUsername("accio2620@gmail.com"); // Replace with your email
            mailSender.setPassword("pzdu zrom dstl iaan");       // Replace with your password

            Properties props = mailSender.getJavaMailProperties();
